import { toast } from "react-toastify";

// FUNCTION TO VALIDATE USER ID'S
export const userIdValidator = (userId) => {
  return userId.match(
    /* eslint-disable  */
    /^(?=.*[A-Za-z])(?=.*\d)(?=.*[@$!%*#?&])[A-Za-z\d@$!%*#?&]{8,}$/
  );
};

// FUNCTION TO VALIDATE EMAILS
export const emailValidator = (email) => {
  return email.match(
    /* eslint-disable  */
    /^(([^<>()[\]\\.,;:\s@\"]+(\.[^<>()[\]\\.,;:\s@\"]+)*)|(\".+\"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/
  );
};


// SUCCESS TOAST
export const toastSuccess = (msg) => {
  toast.success(msg, {
    position: "top-center",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
  });
};

